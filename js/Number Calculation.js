let number = (10 + 5 * 2 - 8 / 4) ** 2 / 2
console.log(number) // 162
/* Operator Precedence in JavaScript (Higher first):

    - Parentheses () → Highest precedence (evaluated first).
    - Exponentiation ** → Right-to-left associativity.
    - Multiplication *, Division / → Left-to-right associativity.
    - Addition +, Subtraction - → Left-to-right associativity.
*/